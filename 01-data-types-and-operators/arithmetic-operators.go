package main

func main() {

	// string concatination
	str := "one" + " " + "two"
	println(str)

	// additon
	add := 1 + 2
	println(add)

	// subtraction
	sub := 10 - 5
	println(sub)

	// remainder
	rem := 5 % 2
	println(rem)

	// increment/decrement
	inc := 1
	inc++
	println(inc)
	inc++
	println(inc)
	inc--
	println(inc)
	inc -= 5
	println(inc)
	inc += 5
	println(inc)
	inc *= 5
	println(inc)
}
